import numpy as np  # Importing the NumPy library

arr = np.array([20, 30, 40, 60])  # Creating a NumPy array

print(np.sum(arr))  # Printing the sum of all elements in the array
print(np.min(arr))  # Printing the minimum value in the array
print(np.max(arr))  # Printing the maximum value in the array
print(np.size(arr))  # Printing the total number of elements in the array
print(np.mean(arr))  # Printing the mean (average) of the array elements
print(np.cumsum(arr))  # Printing the cumulative sum of the array elements
print(np.cumprod(arr))  # Printing the cumulative product of the array elements
